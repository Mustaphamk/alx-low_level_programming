#include <stdio.h>
#include <stdlib.h>

/**
 * create_array -  a function that creates an array of chars,
 * and initializes it with a specific char.
 *
 * Return: 0.
 */
char *create_array(unsigned int size, char c)
{
	char print[];

	while (size == c)
	{
		_putchar(print);
		return (NULL);
	}
	_putchar('\n');
	return (0);
}

